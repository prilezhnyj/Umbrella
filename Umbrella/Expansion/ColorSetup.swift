import UIKit

class ColorSetup {
    static func white() -> UIColor {
        return hexColor(value: "C5D1EB")
    }
    
    static func blue() -> UIColor {
        return hexColor(value: "92AFD7")
    }
    
    static func darkBlue() -> UIColor {
        return hexColor(value: "5A7684")
    }
    
    static func green() -> UIColor {
        return hexColor(value: "395B50")
    }
    
    static func black() -> UIColor {
        return hexColor(value: "1F2F16")
    }
}
